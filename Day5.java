public class Day5 {
    public static void main(String[] args) {
        // mencetak dengan 4 tipe data bilangan bulat
      
        // 1.byte
        byte digit = 10;
        System.out.println("nilai byte : " + digit);
        // 2. short
        short value  = -12;
        System.out.println("nilai short : " + value);
        // 3. Integer
        int age = 19;
        System.out.println("nilai int : " + age);
        // 4. long
        long time = 1225;
        System.out.println("nilai long time : " + time);
    }
}
