public class Day93 {
    public static void main(String[] args) {
        // bulatkan ke atas
        double x =  Math.ceil(12.3);
        System.out.println(x);
        // bulatkan ke bawah
        double y= Math.floor(12.4);
        System.out.println(y);
        // bulatkan terdekat > / <
        double z = Math.round(12.5);
        System.out.println(z);
    }
}
